def largest_number(num1,num2):
    """
    With the help of if,
    we determine a larger 
    number and output it
    """
    if num1 > num2:
        return num1
    else:
        return num2 


def largest_number1(num1,num2):
    """
    With the help of the max method,
    we output a larger number
    """
    return max(num1,num2)